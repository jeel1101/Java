package Practice;

public class Multi2DArray {
    public static void main(String[] args) {
        int[][] houses= new int[2][3];
        houses[0][0]= 51;
        houses[0][1]= 52;
        houses[0][2]= 53;
        houses[1][0]= 54;
        houses[1][1]= 55;
        houses[1][2]= 56;

        System.out.println(houses[0][0]);
        System.out.println(houses[0][1]);
        System.out.println(houses[0][2]);
        System.out.println(houses[1][0]);
        System.out.println(houses[1][1]);
        System.out.println(houses[1][2]);
    }
}
